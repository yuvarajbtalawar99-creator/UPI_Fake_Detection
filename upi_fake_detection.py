    # Invert if necessary (so text is black on white)
    _, count_black = cv2.threshold(thresh, 128, 255, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU)
    if np.mean(count_black) < np.mean(thresh):
        thresh = cv2.bitwise_not(thresh)

    # OCR: EasyOCR if available, else Tesseract
    if _USE_EASYOCR:
        # EasyOCR returns a list of (bbox, text, confidence)
        results = _EASYOCR_READER.readtext(thresh, detail=0)
        text = "\n".join(results)
    else:
        # Tesseract config: LSTM only, assume single block of text
        config = '--oem 1 --psm 6'
        text = pytesseract.image_to_string(thresh, config=config)

    # Patterns for key fields
    amount_pattern = re.compile(r'₹\s*\d{1,7}(?:,\d{3})*(?:\.\d{1,2})?|\bRs\.?\s*\d{1,7}(?:,\d{3})*(?:\.\d{1,2})?\b')
