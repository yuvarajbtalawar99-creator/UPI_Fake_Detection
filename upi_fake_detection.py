        for f in os.listdir(real_dir):
            path = os.path.join(real_dir, f)
            self.samples.append((path, 0))  # 0 = real
        for f in os.listdir(fake_dir):
            path = os.path.join(fake_dir, f)
            self.samples.append((path, 1))  # 1 = fake
